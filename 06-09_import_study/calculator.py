# calculator.py
class Calc:
    def __init__(self, v1, v2):
        self.v1 = v1
        self.v2 = v2


    def add(self):
        return self.v1 + self.v2

    def sub(self):
        return self.v1 - self.v2

    def div(self):
        return self.v1 / self.v2

    def mul(self):
        return self.v1 * self.v2












